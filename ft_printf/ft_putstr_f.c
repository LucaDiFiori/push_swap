#include "ft_printf.h"

int	ft_putstr_f(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (ft_putstr_f("(null)"));
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
